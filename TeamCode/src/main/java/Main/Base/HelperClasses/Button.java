package Main.Base.HelperClasses;

/** Enum of Buttons to use with the BooleanUpdater class.
 *  Couldn't use buttons from gamepad as a key value in the Hashmap in the BooleanUpdater Class/function,
 *  so we're using enums
 */

public enum Button {

    a,
    b,
    x,
    y,
    right_trigger,
    left_trigger,
    right_bumper,
    left_bumper,
    back,
    start,
    dpad_up,
    dpad_down,
    dpad_left,
    dpad_right,
    left_stick_button,
    right_stick_button

}
